#include <iostream> // For cout
#include <vector>

int main()
{
    std::vector<int> knownPrimes = { 2, 3, 5 };
    std::vector<int> desiredOutput;
    const int RTD_LEVEL = 3U;
    for (int input = 1; input < 100; ++input) {
        // Make this NOT a multiple of 2 or 3.
        int output = ((input & ~1U) + (input << 1U)) - 1U;
        if ((output % 5) != 0) {
            desiredOutput.push_back(output);
            std::cout << "(" << desiredOutput.size() << ", " << output << "), ";
        }
    }
    std::cout << std::endl;
    
    for (int output : desiredOutput) {
        // Since this works, just consider it a function to invert.
        int o = output;
        for (int i = RTD_LEVEL - 1; i >= 0; --i) {
            int p = knownPrimes[i];
            o = ((p - 1) * (o + 1)) / p;
        }
        std::cout << o << ", ";
        // std::cout << ((((2 * ((((4 * (output + 1))) / 5) + 1)) / 3) + 1) / 2) << ", ";
    }
    std::cout << std::endl;
}
